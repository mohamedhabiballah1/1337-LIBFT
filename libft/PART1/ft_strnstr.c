#include<stdio.h>
#include<string.h>
#include<xlocale.h>

char    *ft_strnstr(const char *haystack, const char *needle, size_t len)
{
    size_t i;
    size_t j;
    while(haystack[i] != '\0')
    {
        j = 0;
        while((i + j) < len && haystack[i +j] == needle[j])
        {
            if (haystack[i + j] == '\0' && needle[j] == '\0')
                return((char *)&haystack[i]);
            j++;
        }
        if(needle[j] == '\0')
            return((char *)haystack[i]);
        i++;
    }
    return(NULL);
}